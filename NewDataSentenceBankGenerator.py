def generate_sentences(num_sentences, include_entities=True):
    sentences = []
    labels_list = []

    for _ in range(num_sentences):
        if include_entities and random.random() < 0.7:
            # Generate a sentence with an entity
            template = random.choice(entity_sentences)
            entity_type = random.choice(list(entities.keys()))
            entity = random.choice(entities[entity_type])
            sentence = template.replace("{ENTITY}", entity)
            words = sentence.strip('.').split()
            labels = []

            entity_words = entity.split()
            entity_len = len(entity_words)
            entity_start_idx = None

            # Find the index where the entity starts
            for idx in range(len(words) - entity_len + 1):
                if words[idx:idx+entity_len] == entity_words:
                    entity_start_idx = idx
                    break

            for idx, word in enumerate(words):
                if entity_start_idx is not None and entity_start_idx <= idx < entity_start_idx + entity_len:
                    if idx == entity_start_idx:
                        labels.append('B-' + entity_type)
                    else:
                        labels.append('I-' + entity_type)
                else:
                    labels.append('O')
        else:
            # Generate a filler sentence without entities
            sentence = random.choice(filler_sentences)
            words = sentence.strip('.').split()
            labels = ['O'] * len(words)

        # Append period at the end
        words.append('.')
        labels.append('O')

        sentences.append(words)
        labels_list.append(labels)

    return sentences, labels_list

