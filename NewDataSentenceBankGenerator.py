test_split = int(0.8 * len(data))

train_data = data[:train_split]
test_data = data[train_split:test_split]
valid_data = data[test_split:]

# Function to write data to a file in CoNLL format
def write_data(filename, data):
    with open(filename, 'w', encoding='utf-8') as f:
        for words, labels in data:
            for word, label in zip(words, labels):
                f.write(f"{word}\t{label}\n")
            f.write("\n")  # Sentence separator

# Write the datasets to the specified files
write_data('Datasets/GeneratedData/train.txt', train_data)
write_data('Datasets/GeneratedData/test.txt', test_data)
write_data('Datasets/GeneratedData/valid.txt', valid_data)

